    public void populateVaccineJTable() {
        DefaultTableModel model = (DefaultTableModel) vaccineJTable.getModel();

        model.setRowCount(0);
        for (Vaccine vaccine : system.getVaccineDirectory().getVaccineList()) {
            Object[] row = new Object[2];
            row[0] = vaccine.getVaccineId();
            row[1] = vaccine;
            model.addRow(row);
        }
    }

    public void populateVaccineJTable(int vaccId) {
        DefaultTableModel model = (DefaultTableModel) vaccineJTable.getModel();

        model.setRowCount(0);
        for (Vaccine vaccine : system.getVaccineDirectory().getVaccineList()) {
            if (vaccine.getVaccineId() == vaccId) {
                Object[] row = new Object[2];
                row[0] = vaccine.getVaccineId();
                row[1] = vaccine;
                model.addRow(row);
            }
        }
    }

