    private void requestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestJButtonActionPerformed
        // TODO add your handling code here:
        int tempId = 1;
        for (Vaccine vaccine : system.getVaccineDirectory().getVaccineList()) {
            if (tempId == vaccine.getVaccineId()) {
                tempId++;
            }
        }

        if(vaccineNameJTextField.getText().isEmpty() || diseaseJTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Field(s) cannot be empty !!!");
            return;
        }
        Vaccine vaccine = system.getVaccineDirectory().addVaccine(tempId, vaccineNameJTextField.getText(), diseaseJTextField.getText(), 0, null, null);
        try{
        vaccine.setVaccinePrice(Float.parseFloat( priceJTextField.getText()));
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Enter only digits");
            return;
        }
        for (Network network : system.getNetworkDirectory().getNetworkList()) {
            network.getInventoryDirectory().addInventory(network, vaccine, 0);
        }
        vaccine.setManufactureDate(manuDateJTextField.getText());
        vaccine.setExpiryDate(expDateJTextField.getText());
        JOptionPane.showMessageDialog(null, "Vaccine is added!!!");
        populateVaccineJTable();
    }//GEN-LAST:event_requestJButtonActionPerformed

    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = vaccineJTable.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row !!!");
            return;
        }

        Vaccine vaccine = (Vaccine) vaccineJTable.getValueAt(selectedRow, 1);
        system.getVaccineDirectory().removeVaccine(vaccine);
        JOptionPane.showMessageDialog(null, "Removed");
        populateVaccineJTable();

    }//GEN-LAST:event_deleteJButtonActionPerformed

