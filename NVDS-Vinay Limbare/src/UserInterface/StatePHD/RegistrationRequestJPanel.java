    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = registrationRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row !!!");
            return;
        }

        if (registrationRequestJTable.getValueAt(selectedRow, 2) != null) {
            JOptionPane.showMessageDialog(null, "Already assigned !!!");
            return;
        }

        WorkRequest wrk = (WorkRequest) registrationRequestJTable.getValueAt(selectedRow, 1);

        wrk.setReceiver(account);
        populateTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void approveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = registrationRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row !!!");
            return;
        }
        if (registrationRequestJTable.getValueAt(selectedRow, 1).equals("Approved")) {
            JOptionPane.showMessageDialog(null, "Already Approved !!!");
            return;

        }
        WorkRequest wrk = (WorkRequest) registrationRequestJTable.getValueAt(selectedRow, 1);
        wrk.setStatus("Approved");

        UserAccount user = wrk.getSender();

        Network n = null;
        for (Network network : system.getNetworkDirectory().getNetworkList()) {
            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (e == enterprise) {
                    n = network;
                    break;
                }
            }
        }
        HospitalEnterprise e1 = null;
        PharmacyEnterprise e2 = null;
        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof HospitalEnterprise) {
                for (UserAccount us : e.getUserAccountDirectory().getUserAccountList()) {
                    if (user.getUsername().equals(us.getUsername())) {
                        e1 = (HospitalEnterprise) e;
                    }
                }

            }
            if (e instanceof PharmacyEnterprise) {
                for (UserAccount us : e.getUserAccountDirectory().getUserAccountList()) {
                    if (user.getUsername().equals(us.getUsername())) {
                        e2 = (PharmacyEnterprise) e;
                    }
                }

            }
        }
        if (e1 != null) {
            e1.setRegistrationStatus("Approved");
        }
        if (e2 != null) {
            e2.setRegistrationStatus("Approved");
        }
        populateTable();
    }//GEN-LAST:event_approveJButtonActionPerformed

