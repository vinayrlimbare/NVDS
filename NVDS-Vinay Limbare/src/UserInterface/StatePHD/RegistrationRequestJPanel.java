    private void populateTable() {

        DefaultTableModel dtm = (DefaultTableModel) registrationRequestJTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest workRequest : enterprise.getWorkQueue().getWorkRequestList()) {
            if (workRequest instanceof ProviderRegistrationWorkRequest) {
                ProviderRegistrationWorkRequest wrk = (ProviderRegistrationWorkRequest) workRequest;
                Object[] row = new Object[4];
                row[0] = workRequest.getSender().getUsername();
                row[1] = workRequest;
                row[2] = workRequest.getReceiver();
                row[3] = wrk.getProviderType();
                dtm.addRow(row);
            }
        }

    }

